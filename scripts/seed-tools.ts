async function seedTools() {
  console.log('Starting tool seeding...');
  console.log(`Preparing to seed ${tools.length} tools`);

  let successCount = 0;
  let errorCount = 0;

  for (const tool of tools) {
    try {
      // Check if tool already exists
      const { data: existing } = await supabase
        .from('tools')
        .select('id')
        .eq('slug', tool.slug)
        .single();

      if (existing) {
        console.log(`  Skipping ${tool.name} (already exists)`);
        continue;
      }

      // Insert tool
      const { error } = await supabase
        .from('tools')
        .insert([tool]);

      if (error) throw error;

      console.log(` Seeded: ${tool.name} (${tool.category})`);
      successCount++;
    } catch (error) {
      console.error(` Error seeding ${tool.name}:`, error);
      errorCount++;
    }
  }

  console.log(`\n Seeding complete!`);
  console.log(`   - Success: ${successCount} tools`);
  console.log(`   - Errors: ${errorCount} tools`);
  console.log(`   - Total: ${tools.length} tools`);

  // Update category tool counts
  console.log('\nUpdating category counts...');
  const categories = ['writing', 'image', 'video', 'code', 'chat', 'productivity', 'marketing', 'design', 'audio', 'research'];

  for (const category of categories) {
    const { count } = await supabase
      .from('tools')
      .select('*', { count: 'exact', head: true })
      .eq('category', category)
      .eq('status', 'published');

    await supabase
      .from('categories')
      .update({ tool_count: count || 0 })
      .eq('slug', category);

    console.log(` ${category}: ${count || 0} tools`);
  }

  console.log('\n All done!');
}

// Run the seeder
seedTools().catch(console.error);
